        /// <summary>
        /// 为SqlCommand附加参数信息
        /// </summary>
        /// <param name="command">命令</param>
        /// <param name="commandParameters">参数列表</param>
        private static void AttachParameters(SqlCommand command, SqlParameter[] commandParameters)
        {
            foreach (SqlParameter p in commandParameters)
            {
                if ((p.Direction == ParameterDirection.InputOutput) && (p.Value == null))
                {
                    p.Value = DBNull.Value;
                }

                command.Parameters.Add(p);
            }
        }

        /// <summary>
        /// 为参数分配参数值
        /// </summary>
        /// <param name="commandParameters">参数列表</param>
        /// <param name="parameterValues">值列表</param>
        private static void AssignParameterValues(SqlParameter[] commandParameters, object[] parameterValues)
        {
            if ((commandParameters == null) || (parameterValues == null))
            {
                return;
            }

            if (commandParameters.Length != parameterValues.Length)
            {
                throw new ArgumentException("Parameter count does not match Parameter Value count.");
            }

            for (int i = 0, j = commandParameters.Length; i < j; i++)
            {
                commandParameters[i].Value = parameterValues[i];
            }
        }

        /// <summary>
        /// This method opens (if necessary) and assigns a connection, transaction, command type and parameters 
        /// to the provided command.
        /// 打开连接(如果需要)并为命令指定连接信息、事务、命令类型以及参数信息。
        /// </summary>
        /// <param name="command">需要处理的<see cref="SqlCommand"/></param>
        /// <param name="connection">有效的<see cref="SqlConnection"/></param>
        /// <param name="transaction">有效的<see cref="SqlTransaction"/></param>
        /// <param name="commandType">命令类型（存储过程或文本）</param>
        /// <param name="commandText">存储过程名或T-Sql语句</param>
        /// <param name="commandParameters">参数数组</param>
        private static void PrepareCommand(SqlCommand command, SqlConnection connection, SqlTransaction transaction, CommandType commandType, string commandText, SqlParameter[] commandParameters)
        {
            if (connection.State != ConnectionState.Open)
            {
                connection.Open();
            }

            command.Connection = connection;

            command.CommandText = commandText;

            if (transaction != null)
            {
                command.Transaction = transaction;
            }

            command.CommandType = commandType;

            if (commandParameters != null)
            {
                AttachParameters(command, commandParameters);
            }

            return;
        }


