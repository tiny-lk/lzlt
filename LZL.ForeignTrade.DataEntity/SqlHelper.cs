    /// <summary>
    /// 在运行对存储过程的参数进行缓存
    /// </summary>
    public sealed class SqlHelperParameterCache
    {
        #region Privates

        /// <summary>
        /// 默认构造。该类只提供静态的方法，使用私有的构造方法避免手动创建该类的多个实例
        /// </summary>
        private SqlHelperParameterCache()
        {
        }

        /// <summary>
        /// 参数缓存列表。
        /// </summary>
        private static Hashtable _ParamCache = Hashtable.Synchronized(new Hashtable());

        /// <summary>
        /// 从存储过程中查找参数列表
        /// </summary>
        /// <param name="connectionString">有效的连接字符串</param>
        /// <param name="spName">存储过程名</param>
        /// <param name="includeReturnValueParameter">参数中是否包含返回值参数</param>
        /// <returns></returns>
        private static SqlParameter[] DiscoverSpParameterSet(string connectionString, string spName, bool includeReturnValueParameter)
        {
            using (SqlConnection cn = new SqlConnection(connectionString))
            using (SqlCommand cmd = new SqlCommand(spName, cn))
            {
                cn.Open();
                cmd.CommandType = CommandType.StoredProcedure;

                SqlCommandBuilder.DeriveParameters(cmd);

                if (!includeReturnValueParameter)
                {
                    cmd.Parameters.RemoveAt(0);
                }

                SqlParameter[] discoveredParameters = new SqlParameter[cmd.Parameters.Count];
                ;

                cmd.Parameters.CopyTo(discoveredParameters, 0);

                return discoveredParameters;
            }
        }

        /// <summary>
        /// 复制参数列表(深拷贝)
        /// </summary>
        /// <param name="originalParameters">原始参数列表</param>
        /// <returns></returns>
        private static SqlParameter[] CloneParameters(SqlParameter[] originalParameters)
        {
            SqlParameter[] clonedParameters = new SqlParameter[originalParameters.Length];

            for (int i = 0, j = originalParameters.Length; i < j; i++)
            {
                clonedParameters[i] = (SqlParameter)((ICloneable)originalParameters[i]).Clone();
            }

            return clonedParameters;
        }

        #endregion private methods, variables, and constructors

        #region caching functions

        /// <summary>
        /// 将参数数组添加到缓存重
        /// </summary>
        /// <param name="connectionString">有效的连接字符串</param>
        /// <param name="commandText">存储过程名或T-SQL语句</param>
        /// <param name="commandParameters">缓存的参数列表</param>
        public static void CacheParameterSet(string connectionString, string commandText, params SqlParameter[] commandParameters)
        {
            string hashKey = connectionString + ":" + commandText;

            _ParamCache[hashKey] = commandParameters;
        }

        /// <summary>
        /// 从缓存重读取参数列表
        /// </summary>
        /// <param name="connectionString">有效的连接字符串</param>
        /// <param name="commandText">存储过程名或T-SQL语句</param>
        public static SqlParameter[] GetCachedParameterSet(string connectionString, string commandText)
        {
            string hashKey = connectionString + ":" + commandText;

            SqlParameter[] cachedParameters = (SqlParameter[])_ParamCache[hashKey];

            if (cachedParameters == null)
            {
                return null;
            }
            else
            {
                return CloneParameters(cachedParameters);
            }
        }

        #endregion caching functions

        #region Parameter Discovery Functions

        /// <summary>
        /// 得到存储过程的参数列表
        /// </summary>
        /// <remarks>
        /// </remarks>
        /// <param name="connectionString">用来创建<see cref="SqlConnection"/>对象的有效的连接字符串</param>
        /// <param name="spName">存储过程名</param>
        /// <returns>参数数组</returns>
        public static SqlParameter[] GetSpParameterSet(string connectionString, string spName)
        {
            return GetSpParameterSet(connectionString, spName, false);
        }

        /// <summary>
        /// 得到存储过程的参数设置
        /// </summary>
        /// <remarks>
        /// </remarks>
        /// <param name="connectionString">用来创建<see cref="SqlConnection"/>对象的有效的连接字符串</param>
        /// <param name="spName">存储过程名</param>
        /// <param name="includeReturnValueParameter">参数中是否包含返回值</param>
        /// <returns>参数数组</returns>
        public static SqlParameter[] GetSpParameterSet(string connectionString, string spName, bool includeReturnValueParameter)
        {
            string hashKey = connectionString + ":" + spName + (includeReturnValueParameter ? ":include ReturnValue Parameter" : "");

            SqlParameter[] cachedParameters;

            cachedParameters = (SqlParameter[])_ParamCache[hashKey];

            if (cachedParameters == null)
            {
                cachedParameters = (SqlParameter[])(_ParamCache[hashKey] = DiscoverSpParameterSet(connectionString, spName, includeReturnValueParameter));
            }

            return CloneParameters(cachedParameters);
        }

        #endregion Parameter Discovery Functions

