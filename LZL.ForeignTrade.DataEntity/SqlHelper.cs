        /// 根据指定的数据库连接信息执行无返回值的存储过程
        /// </summary>
        /// <remarks>
        /// <example>
        /// <code>
        ///  int result = ExecuteNonQuery(connString, CommandType.StoredProcedure, "PublishOrders", new object[]{"Text"});
        /// </code>
        /// </example>
        /// </remarks>
        /// <param name="connectionString">连接字符串</param>
        /// <param name="spName">存储过程名</param>
        /// <param name="parameterValues">参数值列表</param>
        /// <returns>该命令执行后所影响的行数</returns>
